// Helper function to get IRIS+ metrics for a specific material topic
export const getIRISMetricsByTopic = (topicId: string): IRISMetric[] => {
  return irisMetricsDatabase.filter(metric => 
    metric.materialTopics.some(topic => 
      topic.toLowerCase().includes(topicId.toLowerCase()) ||
      topicId.toLowerCase().includes(topic.toLowerCase())
    )
  );
};

// Helper function to check if a topic is a standard material topic (has IRIS+ metrics)
export const isStandardMaterialTopic = (topicId: string): boolean => {
  return getIRISMetricsByTopic(topicId).length > 0;
};